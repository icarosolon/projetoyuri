        <h2>Acarajé</h2>

        <h3>
            <p>Esse prato traduz o sentimento do povo baiano, uma mistura incrível de sabores que apresentam a identidade de uma terra espetacular. O acarajé é uma especialidade gastronômica da culinária afro-brasileira feita de massa de feijão-fradinho, cebola e sal, frita em azeite-de-dendê e pode ser servido com pimenta, camarão seco, vatapá, caruru ou salada, componentes e pratos típicos, em sua grande maioria, da cozinha local.</p>
            <p>Na Bahia, procure as Baianas do Acarajé e se delicie com a iguaria. Para o Candomblé, o acarajé é considerado uma comida sagrada e sua receita não pode ser modificada. Outro detalhe extremamente importante é que, caso lhe perguntem se você deseja quente ou frio, é referente ao nível de pimenta. Quanto mais quente você pedir esse prato nordestino, mais pimenta virá no seu acarajé, fique ligado.</p>
        </h3>

        <img src="Nordestina/Acaraje.jpg" />

        <h2>Maria Isabel</h2>

        <h3>
            <p>Apesar de estar presente na culinária de outras regiões do Brasil como no Centro-Oeste, a casa da Maria Isabel é o Nordeste. Encontrado no Ceará, Maranhão e Rio Grande do Norte, o prato é muito consumido no Piauí, tanto é que a partir dele, originou-se um festival gastronômico que leva seu nome em Teresina, capital do estado.</p>

            <p>Maria Isabel é o prato nordestino mais representativo da cozinha piauiense, todos os ingredientes são tipicamente regionais, a base de arroz, carne seca, cebola, pimentão, cheiro verde, alho e pimenta-do-reino. Segundo a história contada pelo povo, antigamente só os homens comiam carne seca e no meio das famílias carentes, uma mãe que não tinha o que dar para os filhos, cortou em cubos uma pontinha da carne do pai e fez o prato para toda a família, e batizou assim com o nome das suas filhas, Maria e Isabel.</p>

            <p>Então, já sabe né? Na sua passagem pelo Piauí, não esqueça de apreciar uma boa e legítima Maria Isabel acompanhada de uma cajuína bem gelada.</p>
        </h3>

        <img src="Nordestina/maria-izabel-1.jpg" />

        <h2>Paçoca de Carne Seca</h2>

        <h3>
            <p>Você já deve ter ouvido falar em paçoca, provavelmente aquela que é doce e possui amendoim, mas não é dessa que estamos falando. A paçoca em questão, é a preparada com carne seca desfiada e farinha de mandioca branca. Se você já provou esse prato nordestino, com certeza sentiu o quão difícil foi parar de degustar, se ainda não teve oportunidade, não sabe o que está perdendo!</p>

            <p>De região para região, a forma de produzir a paçoca e consequentemente o seu gosto varia bastante. No Nordeste, os ingredientes já citados são socados no pilão e assim vão pegando o sabor original, mas além desse existem outros modos de preparo, como a carne assada passada com a farinha no liquidificador. Cada variação possui sabor diferenciado e inesquecível.</p>
        </h3>

        <img src="Nordestina/pacoca-nordeste.jpg" />

        <h2>Baião de Dois</h2>

        <h3>
            <p>Com certeza essa é uma das iguarias mais pedidas no Nordeste. Desde seu surgimento no Ceará, o prato conquistou o restante da região e encanta cada vez mais os viajantes. No Nordeste não faltam comidas boas, então, o Baião de Dois feito com leite de coco forma uma ótima combinação junto a paçoca de carne seca ou com qualquer outro prato.</p>

            <p>Há relatos de que em meio as grandes secas e escassez de comida, os cearenses juntavam as sobras de arroz e feijão, com um pouco de carne seca e queijo coalho, cozinhavam tudo junto e assim surgia uma refeição.</p>

            <p>A origem do nome fica por conta da dança típica do nordeste, Baião, e o prato ganhou popularidade com a música Baião de Dois em parceria do compositor cearense Humberto Teixeira e o “Rei do Baião”, Luiz Gonzaga.</p>
        </h3>

        <img src="Nordestina/baiao-de-dois.jpg" />

        <h2>Peixada</h2>

        <h3>
            <p>A peixada é um prato nordestino e você verá que, apesar de levarem o mesmo nome, a peixada de Alagoas, Pernambuco e a do Ceará, possuem diferenças no preparo e no sabor. Neste prato, os elementos presentes na região são muito valorizados, sobretudo os peixes de água salgada. Em alguns lugares, o alimento é preparado com batatas inglesas, molho branco ou de pimenta, leite de coco e muitos os ingredientes.</p>

            <p>O litoral nordestino é extremamente abençoado pela pesca marítima e a maioria das peixadas são preparadas com o peixe fresco, o que a deixa ainda mais especial e saborosa. Esta deliciosa e tradicional iguaria da culinária local, é uma opção fantástica para quem quer mergulhar ainda mais na cultura e raízes da região.</p>
        </h3>

        <img src="Nordestina/peixada-pernambucana.jpg" />

        <h2>Moqueca de Peixe com Pirão</h2>

        <h3>
            <p>Dificilmente encontraremos um brasileiro que nunca tenha ouvido falar ou não tenha se deliciado com uma moqueca. Este prato consiste em um cozido de peixe e outros frutos do mar com diferentes temperos, incluindo em sua receita tradicional o leite de coco e azeite de dendê.</p>

            <p>Detalhe importante é que esse prato nordestino é preparado dentro de uma panela de barro. Apesar de ser típico dos estados de Pernambuco, Espírito Santo e Bahia, o que difere a moqueca de peixe nordestina é o modo de preparo e alguns ingredientes, que possuem influência da culinária africana.</p>

            <p>O pirão consiste na mistura de farinha de mandioca com o caldo do cozimento dos mariscos. Acompanhar a moqueca de peixe com pirão, vai fazer a diferença na hora de apreciar estes pratos que, além de extremamente deliciosos, formam uma combinação fantástica!</p>
        </h3>

        <img src="Nordestina/moqueca-de-peixe-com-pir%C3%A3o.jpg" />

        <h2>Arrumadinho</h2>

        <h3>
            <p>O arrumadinho é um prato genuinamente brasileiro. Feito de um jeito especial que cativa e deixa os turistas com água na boca, além de um nome bonito e convidativo para saborear, a refeição possui diferentes ingredientes em apenas um prato.</p>

            <p>Unindo feijão verde ou o feijão de corda (típico do nordeste), farinha de mandioca torrada, vinagrete e uma carne que costuma ser charque desfiado, esse prato nordestino é preparado com bastante capricho e está presente em cardápios de muitos restaurantes e lanchonetes da região.</p>
        </h3>

        <img src="Nordestina/Arrumadinho.jpg" />

        <h2>Tapioca</h2>

        <h3>
            <p>Comumente presente no café da manhã e no lanche da tarde, a famosa tapioca do nordeste encanta a todos com esse prato pra lá de saboroso. Feita com a fécula da mandioca o que muitos chamam de polvilho, goma ou farinha da mandioca, o melhor da tapioca é que você pode adequá-la ao seu gosto.</p>

            <p>Com ou sem manteiga, com ou sem coco e com recheio de todos os tipos como carne de sol, frango com catupiry, queijo coalho e até chocolate, essas são algumas das delícias que podem ser acrescentadas nesta iguaria.</p>
        </h3>

        <img src="Nordestina/0-tapioca-2472281_1280.jpg" />

        <h2>Cuscuz</h2>

        <h3>
            <p>Assim como a tapioca, o cuscuz está presente diariamente nas mesas nordestinas. Apesar de não possuir origem brasileira, no nordeste o alimento é preparado através da mistura da farinha de milho com sal que posteriormente vai para o vapor. Alguns lugares costumam saborear o cuscuz umedecido com leite de coco ou acompanhado de manteiga que facilmente derrete quando está quente.</p>

            <p>Este prato típico também é degustado em refeições principais como almoço e jantar só que de uma forma bem diferente, pois, substitui o arroz pela farofa de cuscuz. O preparo é bastante simples, o que os nordestinos fazem é cozinhar normalmente o cuscuz, refogar na manteiga diversos tipos de verduras e por último, misturam tudo. Para deixar mais saboroso, vale a pena acrescentar calabresa, bacon e até ovos cozidos na farofa.</p>
        </h3>

        <img src="Nordestina/farofa-de-cuscuz.jpg" />

        <h2>Sarapatel</h2>

        <h3>
            <p>Além de extremamente deliciosa, a gastronomia nordestina é de admirar, frente aos preparos e caprichos que os cozinheiros têm para deixar as comidas no ponto e com sabor impecável. Com o Sarapatel não é diferente, a união de sabores é sensacional.</p>

            <p>O prato típico possui origem portuguesa, mas foi adaptado e com o passar do tempo, foram acrescentados temperos do Nordeste. Assim como a Peixada, ao degustar o Sarapatel de cada um dos estados, é perceptível que a mudança no sabor muda de um local para outro.</p>

            <p>A iguaria está cada vez mais forte nos cardápios dos restaurantes da região e os principais ingredientes que estão presente são as partes mais gordas da barriga de porcos ou carneiros, costelas, fressuras, além de verduras, como cheiro verde, cebola e outras verduras. Com certeza esse é um dos mais exóticos pratos nordestinos.</p>
        </h3>

